return function(F)
  -----------------------------------------------------------------------------
  function F.yellow(fmt, ...)
    if F.DEBUG then
      print("\27[0;33m" .. fmt .. "\27[0m", ...)
    end
  end

  -----------------------------------------------------------------------------
  function F.red(fmt, ...)
    print("\27[1;31m" .. fmt .. "\27[0m", ...)
  end

  -----------------------------------------------------------------------------
  function F.green(fmt, ...)
    print("\27[1;32m" .. fmt .. "\27[0m", ...)
  end

  return F
end
